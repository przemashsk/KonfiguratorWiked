    NUMMER,
    PREISMATRIX,
    KLASSE1,
    KLASSE2,
    FLOOR(BREITE) BREITE,
    FLOOR(HOEHE) HOEHE,
    FLOOR(PREIS) PREIS,
    FLOOR(PREIS2) PREIS2,
    FLOOR(PREIS3) PREIS3,
    FLOOR(PREIS4) PREIS4,
    FLOOR(PREIS5) PREIS5,
    FLOOR(PREIS6) PREIS6,
