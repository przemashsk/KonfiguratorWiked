    PREISMAT
where
    NUMMER = 23031
    and LEFT(PREISMATRIX, 6) = 'DRZWI_'
    AND NOT PREISMATRIX IN('DRZWI_SN', 'DRZWI_RN', 'DRZWI_LP')
    AND BREITE < 9999
    AND HOEHE < 9999 --AND PREIS>0
