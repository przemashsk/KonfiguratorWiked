SELECT A.PRODUKTBEREICH,
A.BEZEICHNUNG,(SELECT PRODUKTBEREICH,
    BEZEICHNUNG,
    PRODUKTTYPEN FROM 
    WIKED..PBEREICH B WHERE A.PRODUKTTYPEN LIKE '%'+B.PRODUKTBEREICH+'%'
    ORDER BY B.PRODUKTTYPEN
    FOR JSON AUTO)B 
FROM 
WIKED..PBEREICH A
WHERE A.PRODUKTBEREICH IN('DRZ','DRZBA','DRZBB')
FOR JSON AUTO